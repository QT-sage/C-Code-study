#include <stdio.h>

int main(){
    //内存
    //char -- 字符类型
    char ch = 'A';
    //%c -- 打印字符格式的数据
    printf("%c\n", ch);

    int age = 20;
    //%d -- 打印整型十进制数据
    printf("%d\n", age);
    return 0;
}

